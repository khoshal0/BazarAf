i18n
  .use(LanguageDetector)  // Detects language from browser/localStorage
  .use(initReactI18next)
  .init({
    resources,
    fallbackLng: 'en',  // Default to English if translation not found
    lng: localStorage.getItem('i18nextLng') || 'en',  // Default to English unless user selects another language
    interpolation: {
      escapeValue: false,  // React already escapes values
    },
    detection: {
      order: ['localStorage', 'navigator'],
      caches: ['localStorage'],
    },
  });

// Listen for language changes and update document direction
i18n.on('languageChanged', (lng: string) => {
  if (lng === 'fa' || lng === 'ps') {
    document.documentElement.dir = 'rtl';
  } else {
    document.documentElement.dir = 'ltr';
  }
  document.documentElement.lang = lng;
  localStorage.setItem('i18nextLng', lng);
  // Emit custom event for App component to listen to
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lng } }));
});

export default i18n;
